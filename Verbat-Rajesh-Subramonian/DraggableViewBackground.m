#import "DraggableViewBackground.h"

@implementation DraggableViewBackground{
    NSInteger cardsLoadedIndex; //%%% the index of the card you have loaded into the loadedCards array last
    NSMutableArray *loadedCards; //%%% the array of card loaded (change max_buffer_size to increase or decrease the number of cards this holds)
    
}
//this makes it so only two cards are loaded at a time to
//avoid performance and memory costs
static const int MAX_BUFFER_SIZE = 2; //%%% max number of cards loaded at any given time, must be greater than 1
static const float CARD_HEIGHT = 386; //%%% height of the draggable card
static const float CARD_WIDTH = 290; //%%% width of the draggable card

@synthesize allCards;//%%% all the cards
@synthesize exampleCardColors;
- (id)initWithFrame:(CGRect)frame
{
    self = [super initWithFrame:frame];
    if (self) {
        [super layoutSubviews];
        [self setupView];
        exampleCardColors = [[NSArray alloc]initWithObjects:@"E34F36",@"991300",@"B6C7BD", nil]; //%%% placeholder for card-specific information

        loadedCards = [[NSMutableArray alloc] init];
        allCards = [[NSMutableArray alloc] init];
        cardsLoadedIndex = 0;
        [self loadCards];
    }
    return self;
}

//%%% sets up the extra buttons on the screen
-(void)setupView
{
    UIToolbar* toolbar = [[UIToolbar alloc] init];
    toolbar.frame = CGRectMake(0, self.frame.size.height - 44, self.frame.size.width, 44);
    UIBarButtonItem *rightSwipe = [[UIBarButtonItem alloc] initWithTitle:@"Swipe-Right" style:UIBarButtonItemStylePlain target:self action:@selector(swipeRight)];
    UIBarButtonItem *flexibleSpace = [[UIBarButtonItem alloc] initWithBarButtonSystemItem:UIBarButtonSystemItemFlexibleSpace target:nil action:nil];
    UIBarButtonItem *leftSwipe = [[UIBarButtonItem alloc] initWithTitle:@"Swipe-Left" style:UIBarButtonItemStylePlain target:self action:@selector(swipeLeft)];
    UIBarButtonItem *reloadbtn = [[UIBarButtonItem alloc] initWithTitle:@"Reload" style:UIBarButtonItemStylePlain target:self action:@selector(loadCards)];

    NSArray *buttonItems = [NSArray arrayWithObjects:leftSwipe,flexibleSpace,reloadbtn,flexibleSpace,rightSwipe,nil];
    [toolbar setItems:buttonItems];
    [self addSubview:toolbar];

}

